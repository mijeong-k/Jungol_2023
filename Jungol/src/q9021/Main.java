package q9021;

public class Main {

	public static void main(String[] args) {
		int a = 10;
		int b = 10;

		System.out.println("최초값 a = " + a + ", b = " + b + "\n");
		System.out.println("a++ = " + a + ", ++b = " + ++b);
		System.out.println("실행후 a = " + ++a + ", b = " + b + "\n");
		System.out.println("a-- = " + a + ", --b = " + --b);
		System.out.println("실행후 a = " + --a + ", b = " + b);

	}

}
